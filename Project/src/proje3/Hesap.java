
package proje3;


public class Hesap {
    
 private int AID;
 private float BALANCE;
 private int CID;

    public Hesap(int AID, float BALANCE, int CID) {
        this.AID = AID;
        this.BALANCE = BALANCE;
        this.CID = CID;
    }

    public int getAID() {
        return AID;
    }

    public void setAID(int AID) {
        this.AID = AID;
    }

    public float getBALANCE() {
        return BALANCE;
    }

    public void setBALANCE(float BALANCE) {
        this.BALANCE = BALANCE;
    }

    public int getCID() {
        return CID;
    }

    public void setCID(int CID) {
        this.CID = CID;
    }

   

 



}
